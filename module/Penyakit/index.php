<p align="justify"><b>1. Busuk Pangkal Batang</b>
</p>
<p align="justify"> Busuk Pangkal Batang merupakan penyakit pada tanaman jeruk yang menyerang batang tanaman jeruk. Penyakit ini disebabkan oleh organisme mirip cendawan Phypthopthora spp. menjadi kendala utama dan menjadi faktor pembatas produksi jeruk nasional. Berikut gejala yang dimiliki oleh penyakit Busuk Pangkal Batang : <br>
    <br>
    a. Menyerang pangkal batang dekat mata tempel.<br>
    b. Bercak basah berwarna gelap pada kulit batang.<br>
    c. Permukaan kulit batang cekung dan mengeluarkan blendok.<br>
    d. Kulit terkelupas menlingkar busuk dan tanaman mati.
</p>

<p align="justify"><b>2. Jamur Upas</b>
</p>
<p align="justify"> Jamur upas adalah jamur penyebab penyakit upas atau mati cabang/ranting. Penyakit Ini biasanya menyerang pohon perkebunan dan buah budidaya di daerah tropis, terutama dimusim penghujan. Berikut gejala yang dimiliki oleh penyakit Jamur Upas : <br>
    <br>
    a. Daun-daun menjadi gugur.<br>
    b. Ranting dan cabang mengalami kematian.<br>
    c. Kulit berubah warna menjadi putih muda/orange.<br>
</p>

<p align="justify"><b>3. Citrus Vein Phloem Degenaration (CVPD)</b>
</p>
<p align="justify"> Citrus Vein Phloem Degenaration (CVPD) merupakan penyakit pada tanaman jeruk yang ditularkan melalui vektor serangga sejenis kutu loncat. Penyakit ini pertama kali dikeluarkan tahun 1929 di Afrika. Berikut gejala yang dimiliki oleh penyakit CVPD : <br>
    <br>
    a. Munculnya belang kuning pada bagian daun.<br>
    b. Bentuk buah tidak simetris.<br>
    c. Buah yang dihasilkan berukuran kecil.<br>
    d. Pangkalan buah berubah warna menjadi orange.<br>
</p>

